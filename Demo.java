// import java.util.*;
public class Demo {

    public static void main(String[] args) {
        System.out.println(args[1]);
    }
}

/*
"public" indicates that the method can be accessed from outside the class.

"static" means the method belongs to the class itself and not to instances of the class.

"void" indicates that the main method does not return any value.

"main" is the name of the method.

"(String[] args)" specifies the parameter that the main method can accept, which in this case is an array of strings representing 
command-line arguments.

*/